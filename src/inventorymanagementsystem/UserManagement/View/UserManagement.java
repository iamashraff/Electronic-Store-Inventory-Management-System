    public void loadTable(){
        lblWelcome.setText("Welcome, " + user.getFirstName() + " " + user.getLastName() + " (" + user.getUsername() + ")" );
        String userRoles = "";
        if(user.getIsAdmin().equals("true")){
            userRoles = "Admin";
            
        }else{
            userRoles = "Staff";
        }
        lblLoginas.setText("You are login as : " + userRoles);
        
        DBConnection dbc = new DBConnection();
        dbc.setQuery("SELECT * FROM users");
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.fireTableDataChanged();
        try{
            Connection con = DriverManager.getConnection(dbc.getUrl(), dbc.getUser(), dbc.getPassword());
            Statement stm = con.createStatement();
            ResultSet res = stm.executeQuery(dbc.getQuery());

            
            int rowcount = 0;
            if (res.last()) {
              rowcount = res.getRow();
              res.beforeFirst(); // not rs.first() because the rs.next() below will move on, missing the first element
            }
            

            String columns[] = { "ID", "First Name", "Last Name", "Admin"};
            String data[][] = new String[rowcount][5];
            
            int i = 0;
            while (res.next()) {
              int id = res.getInt("ID");
              String fname = res.getString("firstname");
              String lname = res.getString("lastname");
              String isAdmin = res.getString("isAdmin");
              String username = res.getString("username");

              data[i][0] = id + "";
              data[i][1] = fname;
              data[i][2] = lname;
              if (isAdmin.equals("true")){
                  data[i][3] = "Admin";
              }else{
                  data[i][3] = "Staff";
              }
              
              data[i][4] = username;
            
              model.addRow(data[i]);
              
              i++;
            }
            
            
              
            
           }catch(SQLException e){
            e.printStackTrace();
        }//END TRY CATCH
        
    }
    
    public void reloadTable(){
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        loadTable();
    }

