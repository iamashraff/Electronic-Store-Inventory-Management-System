                String fname = jTable1.getModel().getValueAt(row, 1).toString();
                String lname = jTable1.getModel().getValueAt(row, 2).toString();
                String isAdmin = jTable1.getModel().getValueAt(row, 3).toString();
                String username = jTable1.getModel().getValueAt(row, 4).toString();
                
                UserManagementModel umd = UserManagementModel.getInstance();
                umd.setID(userID);
                umd.setFirstname(fname);
                umd.setLastname(lname);
                umd.setIsAdmin(isAdmin);
