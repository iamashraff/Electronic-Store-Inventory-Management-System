    public void loadData(String userId){
        this.userId = userId;
        String query = "SELECT * FROM product WHERE id = " + userId;
        dbc.setQuery(query);
        
        try{
                Connection con = DriverManager.getConnection(dbc.getUrl(), dbc.getUser(), dbc.getPassword());
                Statement stm = con.createStatement();
                ResultSet res = stm.executeQuery(dbc.getQuery());

                int rowcount = 0;
                if (res.last()) {
                  rowcount = res.getRow();
                  res.beforeFirst();
                }//END IF
                
                if (rowcount > 0){  
                    
                    int i = 0;                   
                    while (res.next()) {
  
                        txtProdID.setText(userId);
                        txtBrand.setText(res.getString("brand"));
                        txtModel.setText(res.getString("model"));
                        txtStorage.setText(res.getString("storage"));
                        txtRAM.setText(res.getString("ram"));
                        txtPrice.setText(res.getString("price"));
                        txtQuantity.setText(res.getString("quantity"));
                        txtProc.setText(res.getString("processor"));
                        
                        if(res.getString("type").equals("Laptop")){
                            jcbType.setSelectedIndex(0);
                        }else if(res.getString("type").equals("Desktop")){
                            jcbType.setSelectedIndex(1);
                        }else if(res.getString("type").equals("Smartphone")){
                            jcbType.setSelectedIndex(2);
                        }else if(res.getString("type").equals("Tablet")){
                            jcbType.setSelectedIndex(3);
                        }
                        
                        
                        if(res.getString("type").equals("Laptop") || res.getString("type").equals("Desktop"))
                        {
                            txtProc.setVisible(true);
                            lblProc.setVisible(true);
                        }else{
                            txtProc.setVisible(false);
                            lblProc.setVisible(false);
                        }


                      i++;
                    }
                                  
                }else {
                    
                    JOptionPane.showMessageDialog(null, "Error occured", "Error", JOptionPane.ERROR_MESSAGE);
                }//END IF
                
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.toString(), "Error", JOptionPane.ERROR_MESSAGE);
        }//END TRY-CATCH
    }

