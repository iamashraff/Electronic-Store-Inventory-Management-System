    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
try{
        if(jcbType.getSelectedItem().equals("Laptop") || jcbType.getSelectedItem().equals("Desktop")){
            
            Computer comp = new ComputerBuilder()
                    .setType(jcbType.getSelectedItem().toString())
                    .setBrand(txtBrand.getText())
                    .setModel(txtModel.getText())
                    .setStorage(Integer.parseInt(txtStorage.getText()))
                    .setRam(Integer.parseInt(txtRAM.getText()))
                    .setQuantity(Integer.parseInt(txtQuantity.getText()))
                    .setPrice(Integer.parseInt(txtPrice.getText()))
                    .setProcessor(txtProc.getText())
                    .build();
            
            ProductController pc = new ProductController();
            pc.EditProduct(comp,userId);
            
            
        }else{
            Phone phone = new PhoneBuilder()
                    .setType(jcbType.getSelectedItem().toString())
                    .setBrand(txtBrand.getText())
                    .setModel(txtModel.getText())
                    .setStorage(Integer.parseInt(txtStorage.getText()))
                    .setRam(Integer.parseInt(txtRAM.getText()))
                    .setQuantity(Integer.parseInt(txtQuantity.getText()))
                    .setPrice(Integer.parseInt(txtPrice.getText()))
                    .build();
            
            ProductController pc = new ProductController();
            pc.EditProduct(phone, userId);
        }
        dispose();
       }catch(NumberFormatException e){
             JOptionPane.showMessageDialog(null, "Some input require numeric value only. Please try again !", "Input Error", JOptionPane.ERROR_MESSAGE);
         }
    }//GEN-LAST:event_btnAddActionPerformed
    
        
    public void showHideproc(){
    
        if (jcbType.getSelectedItem().toString().equals("Laptop") || jcbType.getSelectedItem().toString().equals("Desktop")){
            txtProc.setVisible(true);
            lblProc.setVisible(true);
        }else{
            txtProc.setVisible(false);
            lblProc.setVisible(false);
        }
    }
    
